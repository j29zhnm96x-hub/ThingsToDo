// Current language (default to English, can be overridden from localStorage)
let currentLang = 'en';
try {
  currentLang = localStorage.getItem('app-language') || 'en';
} catch {
  // Some environments block localStorage access; fall back to English.
  currentLang = 'en';
}

/**
 * Get the current language code
 */
export function getLang() {
  return currentLang;
}

/**
 * Set the current language
 */
export function setLang(lang) {
  if (translations[lang]) {
    currentLang = lang;
    try {
      localStorage.setItem('app-language', lang);
    } catch {
      // ignore
    }
    // Dispatch event so the app can re-render
    try {
      window.dispatchEvent(new CustomEvent('language-changed', { detail: { lang } }));
    } catch {
      // ignore
    }
  }
}

/**
 * Get a translated string by key
 * @param {string} key - The translation key
 * @param {object} params - Optional parameters for interpolation (e.g., {n: 5})
 * @returns {string} The translated string
 */
export function t(key, params = {}) {
  const langStrings = translations[currentLang] || translations.en;
  let str = langStrings[key] || translations.en[key] || `[missing: ${key}]`;
  
  // Handle parameter interpolation like {n}
  for (const [param, value] of Object.entries(params)) {
    str = str.replace(new RegExp(`\\{${param.replace(/[.*+?^${}()|[\]\\]/g, '\\$&')}\\}`, 'g'), value);
  }
  
  return str;
}

/**
 * Get all available language codes
 */
export function getAvailableLanguages() {
  return Object.keys(translations);
}

